//== (Equality operator) -> checks only value
//=== (Strict Equality operator) -> checks value and data type
//Equality operator
console.log(1==1);
console.log(1=="1");
console.log(true==true);
console.log(0=="");
console.log(undefined==null);
console.log(false==0);
console.log(true==1);
//Strict Equality operator
console.log(1===1);
console.log(1==="1");
console.log(true===true);
console.log(0=="");
console.log(undefined===null);
console.log(false===0);
console.log(true===1);

//Non Equality operator
console.log("Non Equality operator");
console.log(1!=1);
console.log(1!="1");
//Strict NonEquality operator
console.log("Strict Non Equality operator");
console.log(1!==1);
console.log(1!=="1");

